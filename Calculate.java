public class Calculate {
    public int calA(int a, int b){
        return a+b;
    }
    public double calB ( double a, double b){
        return a - b;
    }

    public double Null ( double a, double b){
        return a * b;
    }

    public double calC ( double a, double b){
        return a / b;
    }
}

